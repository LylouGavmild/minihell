#include "../../includes/minishell.h"

void	snjoin(char *s, char ***sn, int *i)
{
	if (sn[0][i[1]])
		sn[0][i[1]] = splitjoin(sn[0][i[1]], s[*i]);
	else
	{
		sn[0][i[1]] = malloc(sizeof(char *) * 2);
		if (!sn[0][i[1]])
			exit(1);
		sn[0][i[1]][0] = s[*i];
		sn[0][i[1]][1] = '\0';
	}
}
